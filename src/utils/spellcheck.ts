const lowerCaseWords = new Set(['de', 'la', 'el', 'en', 'y', 'con', 'a', 'las', 'los', 'del', 'al', 'por', 'para']);

// Palabras clave que suelen representar el inicio de una guarnición nueva independiente
const sideDishKeywords = /\b(espagueti|frijoles|arroz|puré|ensalada|tortillas|salsa|totopos|verduras|sopa|crema|caldo|consomé|guacamole|chilaquiles|postre|bebida|agua|pan|galletas)(?=\s|$|[.,;])/gi;

export const fixSpelling = (text: string): string => {
    if (!text) return text;

    // 1. Estandarizar viñetas existentes: "- camarones", "* arroz", "•pure" -> "• camarones", "• arroz", "• pure"
    let cleaned = text.trim().replace(/(?:^|\s)[-*•+]\s*/g, ' • ').trim();

    // Limpieza general de espacios múltiples y minúsculas
    cleaned = cleaned.toLowerCase().replace(/\s+/g, ' ');

    // Separar puntos mal escritos para evitar strings larguísimos que deforman la tabla
    cleaned = cleaned.replace(/\.([a-záéíóúñ])/gi, '. $1');

    // 2. Reemplazos forzados de diccionario PRIMERO para normalizar palabras
    Object.keys(spellFixes).forEach(err => {
        const regex = new RegExp(`\\b${err}\\b`, 'gi');
        cleaned = cleaned.replace(regex, spellFixes[err]);
    });

    // 3. Inyectar separadores lógicos (viñetas "•") antes de guarniciones si faltan separadores
    cleaned = cleaned.replace(sideDishKeywords, (match, p1, offset, fullString) => {
        if (offset === 0) return match;
        
        const before = fullString.slice(0, offset).trimEnd();
        if (before.length === 0) return match;
        
        // Si la palabra anterior es una preposición/conjunción o ya termina en puntuación
        if (/(?:[•\/\-\+\*\,:;]|\b(?:con|de|y|en|la|el|las|los|al|del|su|o|a))\s*$/i.test(before)) {
            return match;
        }
        
        return `• ${match}`;
    });

    // 4. Title Case Inteligente
    const words = cleaned.split(' ').filter(w => w.length > 0);
    let corrected = words.map((word, index) => {
        if (word === '•') return word;
        if (index === 0 || !lowerCaseWords.has(word) || words[index - 1] === '•') {
            return word.charAt(0).toUpperCase() + word.slice(1);
        }
        return word;
    }).join(' ');

    // 5. Limpieza de porciones
    corrected = corrected.replace(/(?:\()?\b(\d+)\s*(?:pz|pza|pzas|piezas|uni|unidades|uds)\b(?:\))?/gi, 
        (m, num) => num === '1' ? '(1 Pza)' : `(${num} Pzas)`);

    corrected = corrected.replace(/\(\s*(\d+)\s*\)/g, 
        (m, num) => num === '1' ? '(1 Pza)' : `(${num} Pzas)`);

    return corrected.replace(/\s+•/g, ' •').replace(/•\s+/g, '• ').replace(/•/g, '• ').replace(/\s+/g, ' ').trim();
};

const proteinKeywords = {
    marisco: /\b(pescado|atun|atún|salmon|salmón|filete|camaron|camarones|ceviche|marisco|mariscos|jaiba|cazon|mojarra|pulpo|aguachile|surimi)\b/i,
    puerco: /\b(puerco|cerdo|pastor|cochinita|pibil|chuleta|chicharron|chicharrón|tocino|longaniza|chorizo|costilla|carnitas|pierna|lomo|buche|jamon|jamón)\b/i,
    res: /\b(res|picadillo|albondiga|albondigas|albóndiga|bistec|arrachera|sirloin|barbacoa|birria|menudo|asado|suadero|t-bone|ribeye|cecina|alambre|machaca|discada|cortadillo|molida)\b/i,
    pollo: /\b(pollo|tinga|pechuga|alitas|nuggets|milanesa|fajitas|alambre de pollo|boneless|pavo|muslo)\b/i
};

export const detectProtein = (name: string): 'pollo' | 'res' | 'puerco' | 'marisco' | undefined => {
    if (!name) return undefined;
    if (proteinKeywords.marisco.test(name)) return 'marisco';
    if (proteinKeywords.puerco.test(name)) return 'puerco';
    if (proteinKeywords.res.test(name)) return 'res';
    if (proteinKeywords.pollo.test(name)) return 'pollo';
    return undefined;
};
